        }
        
        public static object commandsCodes() {
            var f = lineArguments();
            var jsondic = importingTheJson();
            var TheWholeCode = "";
            var intolines = f.readlines();
            foreach (var i in intolines) {
                var line = i.ToList();
                var fullline = "";
                foreach (var i in line) {
                    fullline += i;
                }
                foreach (var i in jsondic) {
                    if (fullline.Contains(base64.b64decode(jsondic[i].encode("ascii")).decode("ascii"))) {
                        Console.WriteLine("Unknown", "'", base64.b64decode(jsondic[i].encode("ascii")).decode("ascii"), "'", "syntax found.\nPlease review your code...");
                        quit();
                    }
                    try {
                        fullline = re.sub(i, base64.b64decode(jsondic[i].encode("ascii")).decode("ascii"), fullline);
                    } catch (Exception) {
                    }
                }
                TheWholeCode += fullline;
            }
            Python_Exec(TheWholeCode);
        }
